                <p>A person who is certified to teach or is already teaching a language professionally may qualify for a professional teaching profile on My language Pro. </p>

                <p>If you apply for a professional teaching profile on My language Pro, we will ask for documentation that proves your training or experience in:</p>
                <ul>
                  <li>
                    Education
                  </li>
                  <li>Language expertise</li>
                </ul>

                <p>Since different countries offer different certification for education and languages, we review each application personally and make our decisions based on all of the information you provide.</p>

                <h6><b>Professional teaching profiles on My language Pro must be able to demonstrate both skills.</b></h6>

                <ul>
                  <li>If you have teaching certification in non-language subjects, then you must also provide documentation that shows your expertise or training in languages.</li>
                  <li>If you have certification as an expert in some aspect of language (linguist, translator, etc), then you must also provide documentation that shows your ability or training as an educator.</li>
                </ul>
                <p>This way, we can confidently recommend you to new students who want professional assistance with their language learning.</p>
                <h6>How to provide documents</h6>

               <p> Please scan and upload a copy of your relevant degrees, certificates, work documents, or other proof of training, experience, or special expertise.</p>
               <p><b>Do not edit your documents</b> after scanning them. They will not be published on your profile. My language Pro will keep your information only for internal reference, so be sure that your name and other identifying information is clearly visible.</p>
