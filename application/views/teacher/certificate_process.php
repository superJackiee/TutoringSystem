               <h6>1. Certificate from an unaccredited institution</h6>

               <p>There are many free online courses available for people to gain skills and knowledge in interesting subjects, but most are not accredited (meaning that they are not held to a certain standard), and so they cannot be used to prove your ability.</p>

               <p>However, they can be used to show students that you are serious about learning and continued self-improvement. These documents are often called names like "Certificate of Achievement", "Certificate of Participation", or "Certificate of Accomplishment".</p>

               <p>For example, the following free courses are usually not accredited:</p>

               <ul>
                 <li>Unaccredited courses from Open2Study.</li>
                 <li>Unaccredited courses from Udemy, Coursera, or other MOOCs.</li>
                 <li>Unaccredited courses from ALISON online courses.</li>
                 <li>Community-organized workshops.</li>
               </ul>

