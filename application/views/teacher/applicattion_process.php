             <h6>1. I forgot to attach certificates/work experience or I want to change my video, can I make changes? </h6>
                <p>Your application will be under review and changes are no longer accepted after submission. Please check your application before submitting it.</p>

                <h6>2. I'm waitlisted. How long till I get further notifications? How long is the waitlist?</h6>

                <p>It could take 2-6 weeks for the language to be open for applications again. Due to the coronavirus, we are receiving more applications than ever. We apologize for the potential delay.</p>

                <h6>3. I applied to be a Professional Teacher but the language is currently closed. Can I change my application to be a Community Tutor?</h6>
                <p>You can only choose one teacher type in your application. Make sure you have checked the open languages and your credentials before submitting your application. If you have submitted a wrong type of application, you can contact us and request to reject your current application so that you can re-apply.</p>
