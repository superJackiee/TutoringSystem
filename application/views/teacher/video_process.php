                <h6><b>2. What should you say?</b></h6>

                <p>Create a script and practice.</p>

                <p>Your video is where you get to show students who you are. Be creative, and please include the following:  </p>

                <h6>1. Who are you</h6>

                <p>Introduce yourself. What is your name, and what languages do you speak? If you have credentials, work experience, or other interesting backgrounds, feel free to share that.</p>

                <h6>2. What are your lessons like?</h6>

                <p>What do you enjoy about teaching, do you have a preferred method, and what is your attitude towards language teaching? You might include specific details about the activities you do during lessons, show materials you use, or teach potential students their first word or phrase!</p>

                <h6>3. Show your personality</h6>

                <ul>
                  <li>
                    <b>Smile </b>- Make students feel welcome to your online classes!
                  </li>
                  <li>
                   <b> Be yourself</b> - Show or tell something interesting about yourself. Do you have special hobbies that students might enjoy discussing with you? Choose clothing or a background that really represents you. 
                  </li>
                </ul>

                <h6>3. Some other tips</h6>
                <ul>
                  <li>
                    <b>Keep it short</b> - Just a few minutes is plenty of time.
                  </li>
                  <li><b>Be natural</b> - Do not read directly from a script! Look into the camera, make it as though you are speaking directly to your student.</li>
                  <li>
                   <b> Speak in all the languages in which you are fluent</b> - You don’t need to say the same thing again in every language, but you can move from one language to the next as you speak. We recommend adding subtitles so that students can more easily tell what you are saying in each language.
                  </li>
                  <li>
                   <b> Think of your students </b> - The care you take in preparing for your video shows students not only what you think of yourself, but how they can expect you to prepare for your classes with them.
                  </li>
                  <li>
                   <b> Mention My language Pro</b> - Doing this and adding a link to your teacher profile in the description will help people find and book your classes on My language Pro. 
                  </li>
                  <li>
                   <b> Dress Appropriately</b> - Smart casual is acceptable, remember that 1st impressions count
                  </li>
                </ul>
